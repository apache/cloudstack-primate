<template>
  <a-spin :spinning="fetchLoading">
    <a-tabs :tabPosition="device === 'mobile' ? 'top' : 'left'" :animated="false">
      <a-tab-pane v-for="nsp in hardcodedNsps" :key="nsp.title">
        <span slot="tab">
          {{ $t(nsp.title) }}
          <status :text="nsp in nsps ? nsps[nsp.title].state : 'Disabled'" style="margin-bottom: 6px; margin-left: 6px" />
        </span>
        <component
          :is="nsp.component"
          :nsp="nsps[nsp.title]"
          :actions="nsp.actions"
          :bordered="false"
        />
      </a-tab-pane>
    </a-tabs>
  </a-spin>
</template>

<script>
import { api } from '@/api'
import { mixinDevice } from '@/utils/mixin.js'
import Status from '@/components/widgets/Status'

export default {
  name: 'ServiceProvidersTab',
  components: {
    Status
  },
  mixins: [mixinDevice],
  props: {
    resource: {
      type: Object,
      required: true
    },
    loading: {
      type: Boolean,
      default: false
    }
  },
  data () {
    return {
      nsps: {},
      details: ['name', 'state', 'id', 'servicelist'],
      fetchLoading: false
    }
  },
  computed: {
    hardcodedNsps () {
      return [
        {
          title: 'BaremetalDhcpProvider',
          component: () => import('@/views/infra/network/providers/BaremetalDhcpProvider.vue')
        },
        {
          title: 'BaremetalPxeProvider'
        },
        {
          title: 'BigSwitchBcf'
        },
        {
          title: 'BrocadeVcs'
        },
        {
          title: 'CiscoVnmc'
        },
        {
          title: 'ConfigDrive'
        },
        {
          title: 'F5BigIp'
        },
        {
          title: 'GloboDns'
        },
        {
          title: 'InternalLbVm'
        },
        {
          title: 'JuniperSRX'
        },
        {
          title: 'Netscaler'
        },
        {
          title: 'NiciraNvp'
        },
        {
          title: 'Opendaylight'
        },
        {
          title: 'Ovs'
        },
        {
          title: 'PaloAlto'
        },
        {
          title: 'SecurityGroupProvider'
        },
        {
          title: 'VirtualRouter'
        },
        {
          title: 'VpcVirtualRouter'
        }
      ]
    }
  },
  mounted () {
    this.fetchData()
  },
  watch: {
    loading (newData, oldData) {
      if (!newData && this.resource.id) {
        this.fetchData()
      }
    }
  },
  methods: {
    fetchData () {
      this.fetchServiceProvider()
    },
    fetchServiceProvider (name) {
      this.fetchLoading = true
      api('listNetworkServiceProviders', { physicalnetworkid: this.resource.id, name: name }).then(json => {
        var sps = json.listnetworkserviceprovidersresponse.networkserviceprovider || []
        if (sps.length > 0) {
          for (const sp of sps) {
            this.nsps[sp.name] = sp
          }
        }
      }).catch(error => {
        this.$notification.error({
          message: 'Request Failed',
          description: error.response.headers['x-description'],
          duration: 0
        })
      }).finally(() => {
        this.fetchLoading = false
      })
    }
  }
}
</script>

